    <h2>Components of the heart</h2>
    <p>The heart is a conical muscle about the size of a fist. It is hollow from the inside. Its base is located at the top, while the apex faces down almost to the left. The weight of an adult heart is approximately 300 grams. The length of the heart muscle is 12 cm, while its width is 9 cm at its widest point. Its thickness reaches 6 cm, and it is surrounded by a membrane called the pericardium that protects it, facilitates its movement, and holds it in place, as more than two-thirds of it deviates to the left side of the body.</p>
    
    <h3>Four Chambers of the Heart</h3>
    <ul>
        <li><strong>Right atrium:</strong> It is located in the right part of the heart and receives deoxygenated, i.e. low-oxygen, blood from the superior vena cava and inferior vena cava.</li>
        <li><strong>Right ventricle:</strong> It occupies the largest part of the internal space of the heart, and is separated from the right atrium by a valve called the tricuspid valve. It receives deoxygenated blood from the right atrium.</li>
        <li><strong>Pulmonary:</strong> This deoxygenated blood is pumped to the lungs to be replaced with blood loaded with oxygen, to return to the heart through the pulmonary veins. There is also a pulmonary semi-lunar valve between the right ventricle and the pulmonary artery that prevents blood from returning from the pulmonary artery to the right ventricle.</li>
        <li><strong>Left atrium:</strong> Receives oxygenated blood coming from the lungs and transports it to the left ventricle.</li>
        <li><strong>Left ventricle:</strong> It forms the apex of the heart. It receives oxygenated blood from the left ventricle and is separated by a valve called the bicuspid valve, which prevents blood from returning from the left ventricle to the left atrium. The left ventricle pumps blood into the aorta to transport blood to all parts of the body.</li>
